<h2>3. Принципи менеджменту</h2>

<p>
Управління підприємством здійснюється на основі певних принципів.
Вони забезпечують ефективність діяльності організації.
</p>

<ul>

<li>науковість;</li>

<li>системність;</li>

<li>економічність;</li>

<li>відповідальність;</li>

<li>єдність керівництва;</li>

<li>поєднання централізації та децентралізації;</li>

<li>дисципліна;</li>

<li>цілеспрямованість.</li>

</ul>

</section>

<section>

<h2>4. Рівні менеджменту</h2>

<p>
У сучасних організаціях існує три рівні управління.
</p>

<ul>

<li><b>Вищий рівень</b> — генеральний директор, директор, президент компанії.</li>

<li><b>Середній рівень</b> — начальники відділів та керівники структурних підрозділів.</li>

<li><b>Нижчий рівень</b> — майстри, бригадири, керівники робочих груп.</li>

</ul>

</section>

<section>

<h2>5. Методи управління</h2>

<p>
Методи управління — це способи впливу керівника на працівників для досягнення
цілей підприємства.
</p>

<ul>

<li><b>Адміністративні</b> — накази, розпорядження, інструкції.</li>

<li><b>Економічні</b> — премії, заробітна плата, матеріальне стимулювання.</li>

<li><b>Соціально-психологічні</b> — моральне заохочення, формування командного духу.</li>

</ul>

</section>

