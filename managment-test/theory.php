<h2>8. Планування</h2>

<p>
Планування є однією з найважливіших функцій менеджменту.
Воно дозволяє визначити майбутній розвиток підприємства.
</p>

<ul>

<li>Стратегічне планування — на 3–10 років.</li>

<li>Тактичне планування — на 1–3 роки.</li>

<li>Оперативне планування — до одного року.</li>

</ul>

